#import "YDMessageModel.h"

@implementation YDMessageModel

- (void)setValue:(id)value forKey:(NSString *)key
{
    if ([key isEqualToString:@"isRead"]) {
        _isRead = [value boolValue];
    } else {
        [super setValue:value forKey:key];
    }
}

@end
