#import "YDCustListModel.h"

@implementation YDCustListModel

- (void)setValue:(id)value forKey:(NSString *)key
{
    if ([key isEqualToString:@"quoteMoney"]) {
        _quoteMoney = [NSString stringWithFormat:@"%@", value];
    } else {
        [super setValue:value forKey:key];
    }
}

@end
